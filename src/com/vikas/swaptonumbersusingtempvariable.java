package com.vikas;

public class swaptonumbersusingtempvariable {
public static void main(String...args) {
	int x = 1 ;
	int y = 2 ;
	int temp;
	temp=x; // x is 1
	x=y; // y is 2
	y=temp;	// y is 1
	System.out.println("x:"+x);
	System.out.println("y;"+y);
	
	
	int a = 20 ;
	int b = 30 ;
	a=a-b;  // a = 20-30 = -10
	b=a+b; // b = -10+30 = 20
	a=b-a; // a = 20-(-10) = 30
	
	System.out.println("x:"+x);
	System.out.println("y;"+y);


}

	





}

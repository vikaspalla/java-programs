package com.vikas;

public class Switch1 {
	public static void main(String[]args) {
		int n = 2;
				
				switch(n)
				{
				case 1:
				System.out.println("one");
				break;
				case 2 :
					System.out.println("two");
					break;
				case 3 :
					System.out.println("three");
					break;
				case 4 :
					System.out.println("four");
					break;
					
					
	
}
				
	
		
		
	}

}

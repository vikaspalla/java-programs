package com.vikas;

public class Switch {
	public static void main(String[]args) {
		int n = 3;
		
		switch(n)
		{
		case 1:
		System.out.println("one");
		case 2 :
			System.out.println("two");
		case 3 :
			System.out.println("three");
		break ;
		case 4 :
			System.out.println("four");
		default :
			System.out.println("nomatch");
		}
		
		
		
		
		
		
	}

}

package com.vikas;

public class IterationFor {
	public static void main(String...args) {
		//for 
		// no need 3 conditions 
		// using for loop -> all 3 conditions can write in one statement
		
		for(int i=1; i<=5; i++)
		{
		System.out.println("hello");
		}
		
		
		// if we know starting and ending point always go for for loop
		// if we have to take input from user continously until he says exits , go for while loop
		// do while- even if our condition is false , but we want to excute , go for do while loop
	}

}



package com.vikas;

public class Conversion {

	public static void main(String[] args) {
		
		
		int a = 10;
		System.out.println(a);
		float b = a; // Implicit conversion
		System.out.println(b);
		
		float c = 23.33f;
		int d = (int) c; //Explict conversion
		System.out.println(d);
		
		int num = 3;
		System.out.println(num);
	}
}
